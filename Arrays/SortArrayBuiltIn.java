public class SortArrayBuiltIn {
    public static void main(String[] args) {
        //sort - ascending order 
        int[] arr = {4,3,10,46,2,98,0};
        print(arr);
    }
    public static void print(int[] arr){
        for(int i=0;i<arr.length;i++){
            System.out.print(arr[i]+ " ");
        }
        System.out.println();
    }
}
